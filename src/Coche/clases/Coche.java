package Coche.clases;

public class Coche {
    int cantPuertas;
    String marca;
    int anio;
    int ltGasolina;

    public Coche() {
    }
    public Coche(int cantPuertas, String marca, int anio, int ltGasolina) {
        this.cantPuertas = cantPuertas;
        this.marca = marca;
        this.anio = anio;
        this.ltGasolina = ltGasolina;
    }

}
